-- | Internal helper kept for potential future use: format a list of
-- string-literal alternatives for a TypeScript string-union type.
_tsStringUnion :: [Text] -> Text
_tsStringUnion xs = T.intercalate " | " (map (\t -> "\"" <> t <> "\"") xs)
