{-# LANGUAGE OverloadedStrings #-}
{-# LANGUAGE LambdaCase #-}

-- |
-- Module      : LeadProfiler.Emit.TypeScript
-- Description : Emit TypeScript type definitions from the Haskell ADTs.
--
-- The original Next.js project has a @lib/lead-profiler-types.ts@ file
-- that defines the TypeScript types for the three routes' outputs. That
-- file is hand-maintained and can drift from the actual JSON the LLM
-- produces — different field name conventions, different enum values, etc.
--
-- This module generates TypeScript type definitions /directly/ from the
-- Haskell ADTs, so the schema is defined in one place (the Haskell
-- source). The output can be written to a @.ts@ file and imported by the
-- Next.js app.
module LeadProfiler.Emit.TypeScript
  ( emitTypeScript
  , tsLeadObservation
  , tsProspectAudit
  , tsUserServices
  ) where

import           LeadProfiler.Prelude
import           LeadProfiler.Schemas.LeadObservation  (CallToAction(..))
import           LeadProfiler.Schemas.ProspectAudit
import           LeadProfiler.Schemas.Common          (TriBool(..), Confidence(..))

import qualified Data.Text as T

-- | Generate the full TypeScript definitions file as 'Text'.
emitTypeScript :: Text
emitTypeScript = T.unlines
  [ "// ---------------------------------------------------------------------------"
  , "// Auto-generated by lead-profiler-haskell Emit.TypeScript."
  , "// Do not edit by hand — edit the Haskell ADTs and re-run the generator."
  , "//"
  , "// These types mirror the JSON the lead-profiler-haskell validators produce."
  , "// The Next.js app can import them directly to get type-safe access to the"
  , "// validated VLM/LLM output."
  , "// ---------------------------------------------------------------------------"
  , ""
  , tsLeadObservation
  , ""
  , tsProspectAudit
  , ""
  , tsUserServices
  ]

